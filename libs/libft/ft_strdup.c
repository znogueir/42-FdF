#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*result;
	int		i;
	int		len;

	len = ft_strlen(s) + 1;
	result = malloc(sizeof(char) * len);
	if (!result)
		return (0);
	i = 0;
	while (s[i])
	{
		result[i] = s[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
